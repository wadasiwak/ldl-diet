/** 依人工表回傳常見一份克數；關鍵字長者優先，未命中回 DEFAULT_GRAMS。 */
export function portionFor(name) {
  let best = null
  for (const [kw, g] of PORTION_RULES) {
    if (name.includes(kw) && (best === null || kw.length > best[0].length)) {
      best = [kw, g]
    }
  }
  return best ? best[1] : DEFAULT_GRAMS
}
